float rms(float sinal[], int num) {
	float integral = 0;
	float rms = 0;

	int y;
	for (y = 0; y < num; y++) {
		integral += (sinal[y] * sinal[y]);
	}

	integral = integral / num;
	rms = sqrt(integral);

	return rms;
}

void FirstOrderIIR_Init(FirstOrderIIR *filt, float alpha) {
	if (alpha < 0.0f) {
		filt->alpha = 0.0f;
	} else if (alpha > 1.0f) {
		filt->alpha=1.0f;
	} else {
		filt->alpha = alpha;
	}

	filt->out = 0.0f;
}

float FirstOrderIIR_Update(FirstOrderIIR *filt, float in) {
	filt->out = (1.0f - filt->alpha) * in + filt->alpha * filt->out;
	return filt->out;
}

